    private void rbDemCivilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rbDemCivilMouseClicked
        rbDemLab.setSelected(false);
        rbDemCivil.setSelected(true);
        cbTipoDem.removeAllItems();
        cbTipoDem.addItem("Mercantil");
        cbTipoDem.addItem("Familiar");
        cbTipoDem.addItem("Civil");
        cbTipoDem.addItem("Penal");
        cbTipoDem.addItem("Fiscal");
    }//GEN-LAST:event_rbDemCivilMouseClicked

    private void rbDemLabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rbDemLabMouseClicked
        rbDemLab.setSelected(true);
        rbDemCivil.setSelected(false);
        cbTipoDem.removeAllItems();
        cbTipoDem.addItem("Actor");
        cbTipoDem.addItem("Demandado");
    }//GEN-LAST:event_rbDemLabMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FrameInternoAltaDemandaLaboral fadl=new FrameInternoAltaDemandaLaboral();
        JDesktopPane desk=getDesktopPane();
        Dimension desktopSize = desk.getSize();
        Dimension jInternalFrameSize = fadl.getSize();
        fadl.setLocation((desktopSize.width - jInternalFrameSize.width) / 2, (desktopSize.height - jInternalFrameSize.height) / 2);      
        desk.add(fadl);
        fadl.show();
        
          this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed


